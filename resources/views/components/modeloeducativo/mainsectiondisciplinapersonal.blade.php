                    <h3> <label class="newtitle">Disciplina personal</label></h3>
                    <p class="text-justify text-blue-sedi">
                        En SEDI fomentamos en los niños la disciplina personal desde sus primeros años y contemplamos bajo este rubro tres aspectos que son:

                    </p>
                    <br>
                    <p>
                        <ul class="text-blue-sedi">
                            <li>
                                El <strong>establecimiento de hábitos saludables</strong> de sueño, higiene, alimentación y actividad física,
                            </li>
                            <li>
                                La <strong>perseverancia</strong>, entendida como la capacidad de actuar ordenadamente para alcanzar metas y propósitos, y
                            </li>
                            <li>
                                El <strong>seguimiento</strong> de reglas para la convivencia armónica
                            </li>
                        </ul>
                    </p>
                    <br>
                    <p class="text-justify text-blue-sedi">
                        Para lograrlo <strong>nos apoyamos en los estudios y propuestas de dos corrientes psicológicas y educativas que se intersectan en sus objetivos</strong> y que son:

                    </p>
                    <p>
                        <br>
                        <ul class="text-blue-sedi">
                            <li>
                                La <strong>Disciplina Positiva</strong>, que es una metodología planteada por las psicólogas Jane Nelsen y Lynn Lott,
                                sustentada en la filosofía de los doctores Alfred Adler y  Rudolf Dreikurs. A través de su implementación,
                                enseña a los niños a ser responsables, respetuosos y auto-disciplinados dentro de su comunidad.
                                <strong>Este tipo de disciplina es fundamental para guiar la conducta de los niños de una manera respetuosa y firme,
                                basada en el establecimiento de límites claros y consistentes.</strong> Además, se les presentan a los niños
                                alternativas para actuar correctamente en cada situación. Los niños requieren límites claros y figuras de
                                autoridad que le den certeza y seguridad para desarrollarse adecuadamente dentro de su familia, grupo, comunidad y
                                finalmente en una sociedad.
                            </li>
                            <li>
                                La <strong>Disciplina Consciente</strong>, que es un programa socioemocional propuesto por la Dra. Becky Bailey, experta en educación,
                                y que <strong>enseña a los niños cómo regular y manejar sus emociones para tomar decisiones seguras y saludables.</strong>
                                Su enfoque fundamental está en los padres y demás adultos que rodean al niño. En otras palabras:
                                para ayudar mejor a nuestros niños, <strong>primero debemos hacer el trabajo los adultos encargados de los niños (padres y maestros)
                                en nuestras propias emociones, para enseñar con el ejemplo además de con las palabras.</strong> Solo de esta manera podremos
                                estar capacitados para ayudar a los niños a identificar sus emociones y a trabajar sobre ellas; a reflexionar en
                                sus acciones y en sus consecuencias. El segundo elemento indispensable de este programa es la conexión o vínculo
                                 entre los adultos y los niños.
                            </li>
                        </ul>
                    </p>
